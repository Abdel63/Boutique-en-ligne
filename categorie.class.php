<?php

class categorie
{
    private $_id;
    private $_nom;
    private $_description;
    private $_image;

    function __construct($nom, $description, $image)
    {

        $this->_id = $id;
        $this->_nom = $nom;
        $this->_description = $description;
        $this->_image = $image;
    }
}
